# 수식(numerical)-----------------------------
print(2 + 3 * 4)  # 14
print((2 + 3) * 4)  # 20 괄호처리 먼저계산

number = 2 + 3 * 4  # 14 변수에 수넣기
print(number)
number = number + 2  # 16
print(number)

number += 2  # 18  변수에 +2해서 변수에 다시 저장
print(number)
number -= 2  # 16  변수에 -2해서 변수에 다시 저장
print(number)
number *= 2  # 32  변수에 *2해서 변수에 다시 저장
print(number)
number /= 2  # 16  변수에 /2해서 변수에 다시 저장
print(number)

number = 23
number %= 5  # 0  변수를 5로나누고 "남은 나머지" 변수에 다시 저장
print(number)
number != 7  # 0  변수를???????
print(number)


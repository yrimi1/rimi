#숫자 int 자료형-----------------------------------
# 파이썬은 자료형 써줄 필요 없이 숫자 바로입력.실수도 가능
print(1)
print(3.24)
#문자 str자료형-----------------------------------------
# 문자 쓸때는 따옴표에 넣어라 작은따옴표큰따옴표 둘다 가능
print('풍선')
print("풍선"*10) #이런식으로 표현 가능
# boolean 불리안은 참 거짓------------------------------------

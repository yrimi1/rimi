# 응집도 : 명령어나 호출문 같은 모듈 내부요소간의 관련정도..즉 모듈이 독립적으로 정의되어있는정도
# 공통모듈은 여기저기서 재사용가능한모듈 자주사용되는 계산식 ,매번필요한 사용자인증 같은 기능들로 구성



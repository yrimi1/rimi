# 모듈이란 서브루틴, 서브시스템,소프트웨어 내의 프로그램 , 작업단위 와 같은의미
# 서브루틴이란 메인루팅으로부터 필요할떄마다 호출되는 루틴
# 모듈은 독립성을 가져야한다.다른모듈과의 상호작용이 과하면 수정이 곤란해짐.
# 모듈의 독립성은 결합도 와 응집성으로 측정한다. 독립성을 높이려면 결합도를 약하게, 응집도는 강하게, 모듈의 크기는 작아야한다

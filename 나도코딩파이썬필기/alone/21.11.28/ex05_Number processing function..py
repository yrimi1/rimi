# 숫자 처리 함수 (Number processing function) ---------------------------

print(abs(-999))  # 절대값(Absolute)
print(pow(4, 2))  # 4^2 4를 2번 거듭제곱한 값 16출력 (=4**2)
print(max(5, 12))  # 둘 중  큰값 반환
print(min(5, 12))  # 둘 중  작은값 반환
print(round(3.14))  # 반올림 # 3  #round는 수에따라서 올리고버림 하는 수식계산
print(round(4.99))  # 5

from math import *
#파이썬 제공 메쓰 라이브러리
print(floor(4.99)) # 강제 내림
print(ceil(3.14)) #강제 올림
print(sqrt(16)) #제곱근(에스큐알티)



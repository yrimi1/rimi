public class alone {
    public static void main(String[] args) {
        int a = 5;
        int b = 3;


        // if문 (조건 true면 중괄호 실행 )
        if (a < b)
            System.out.println("b가 더 큽니다.a =" + a + "b =" + b);
            //아.. 자바는 콤마대신 더하기

        else
            System.out.println("a가 더 큰가봄 a = " + a + "b = " + b);

        // if 문 조건 확인해서  true 면 중괄호 실행 , false면 if문 뛰어 넘고 else 중괄호 실행
    }
}
